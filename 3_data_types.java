//Primitive data types
//byte - -128 to 127
byte myNum = 100;
//short - -32768 - 32767
short myNum = 5000;
//int - -2,147,483,648 to 2,147,483,647
int myNum = 100000;
//long -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
//End the value with an "L":
long myNum = 15000000000L;
//float - fractional numbers, 6 - 7 decimal digits
//End the value with an "f" 
float myNum = 5.75f;
//double - fractional numbers, 15 decimal digits
//End the value with an "d" 
double myNum = 19.99d;
//boolean - true or false
boolean yes = true;
boolean no = false;
//char - single character or ASCII values
char myGrade = "B";
char myVar1 = 65; //ASCII

//Non-primitive data types
//String
//Arrays
//Classes

//Scientific Numbers
float f1 = 35e3f;
double d1 = 12E4d;