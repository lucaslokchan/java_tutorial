//while loop
while (condition) {
    // code block to be executed
}

//example
int i = 0;
while (1 < 5) {
    System.out.println(i);
    i++;
}

//do while loop
//This loop will execute the code block once, before checking if the condition is true, then it will repeat the loop as long as the condition is true
do {
    // code block to be executed
}
while (condition);

//example
int i = 0;
do {
  System.out.println(i);
  i++;
}
while (i < 5);